#include "libft.h"

int	ft_intlen(int i)
{
	size_t	len;

	len = 0;
	if (i == -2147483648)
		i++;
	if (i < 0)
	{
		len++;
		i *= -1;
	}
	while (i > 9)
	{
		i /= 10;
		len++;
	}
	len++;
	return (len);
}
